var cartData=JSON.parse(localStorage.getItem("cart"))|| [];
var cartData1= JSON.parse(localStorage.getItem("wish")) || [];
womensData.map(function (elem){
    var box=document.createElement("div")
    var img=document.createElement("img")
    img.src=elem.imageUrl;
    var name=document.createElement("p")
    name.innerText=elem.name;
    var brand=document.createElement("p")
    brand.innerText=elem.brand;
    var price=document.createElement("p")
    price.innerText=elem.price;
    var btn=document.createElement("button");
    btn.innerText="ADD TO BAG";
    btn.setAttribute('id','btn')
    var btn2=document.createElement("button");
    btn2.innerText="ADD TO WISHLIST";
    
    btn.addEventListener("click",function(){
        addToCart(elem);
    })
    btn2.addEventListener("click",function(){
        addToCart1(elem);
    })

    box.append(img,name,brand,price,btn,btn2);
    document.querySelector("#womens").append(box)

})
function addToCart(elem){
    cartData.push(elem);
    alert("Added Product Succesfully")
    localStorage.setItem("cart",JSON.stringify(cartData));
}
function addToCart1(elem){
    cartData1.push(elem);
    alert("Added Product in Wishlist")
    localStorage.setItem("wish",JSON.stringify(cartData1))
}
const addCart =document.querySelectorAll("#btn")
const cartShow =document.querySelector('.num span');

for(var i=0;i<addCart.length;i++){
    addCart[i].addEventListener('click',() =>{
        aCart();
    })
}
function aCart(){
    let prdCount =localStorage.getItem('cartsCount');
    prdCount =parseInt(prdCount)
    if(prdCount){
        localStorage.setItem('cartsCount', prdCount+1);
        cartShow.textContent = prdCount +1;
    }
    else{
        localStorage.setItem('cartsCount',1);
        cartShow.textContent = prdCount =1;
    } 
}
function displayCart(){
    let prdCount =localStorage.getItem('cartsCount');
    if(prdCount){
        cartShow.textContent = prdCount;
    }
}
displayCart();
